// Accordion One JS
$("#accordion_one").click(function() {
    if ($("#dropd_one").hasClass("hide")) {
        $("#dropd_one").removeClass("hide");
    }
    else {
        $("#dropd_one").addClass("hide");
    }
})

$("#accordion_one").click(function() {
    if ($("#accordion_one").hasClass("active")) {
        $("#accordion_one").removeClass("active");
    }
    else {
        $("#accordion_one").addClass("active");
    }
})
